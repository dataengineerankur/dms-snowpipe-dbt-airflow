-- ============================================================================
-- Snowflake University Foundation Dataset
-- Concept:
--   Build a realistic shared e-commerce dataset that powers every downstream
--   Snowflake concept in this repository.
-- Why it matters:
--   Production Snowflake design choices only become visible when table sizes,
--   partition counts, and refresh patterns are large enough to create real scan,
--   pruning, and credit-consumption differences.
-- What to look for in Snowflake UI:
--   Query profile for batched inserts, bytes written, micro-partition counts,
--   clustering information, and warehouse load patterns.
-- Common mistakes / anti-patterns:
--   1. Loading everything in one monolithic statement on a tiny warehouse.
--   2. Using non-configurable row counts that are too expensive for dev.
--   3. Skipping verification queries, then debugging downstream failures blind.
-- ============================================================================

USE ROLE ACCOUNTADMIN;
USE WAREHOUSE COMPUTE_WH;

CREATE DATABASE IF NOT EXISTS RAW_DB;
CREATE DATABASE IF NOT EXISTS STAGING_DB;
CREATE DATABASE IF NOT EXISTS SILVER_DB;
CREATE DATABASE IF NOT EXISTS GOLD_DB;
CREATE DATABASE IF NOT EXISTS SNAPSHOTS_DB;
CREATE DATABASE IF NOT EXISTS BENCHMARKS;
CREATE DATABASE IF NOT EXISTS AUDIT_DB;

CREATE SCHEMA IF NOT EXISTS RAW_DB.PUBLIC;
CREATE SCHEMA IF NOT EXISTS STAGING_DB.PUBLIC;
CREATE SCHEMA IF NOT EXISTS SILVER_DB.PUBLIC;
CREATE SCHEMA IF NOT EXISTS GOLD_DB.PUBLIC;
CREATE SCHEMA IF NOT EXISTS SNAPSHOTS_DB.PUBLIC;
CREATE SCHEMA IF NOT EXISTS BENCHMARKS.PUBLIC;
CREATE SCHEMA IF NOT EXISTS AUDIT_DB.PUBLIC;

SET ORDERS_TARGET_ROWS    = 50000000;
SET EVENTS_TARGET_ROWS    = 100000000;
SET PRODUCTS_TARGET_ROWS  = 200000;
SET CUSTOMERS_TARGET_ROWS = 500000;
SET BATCH_SIZE            = 1000000;
SET START_TS              = CURRENT_TIMESTAMP();

CREATE OR REPLACE TABLE RAW_DB.PUBLIC.RAW_ORDERS (
  ORDER_ID      VARCHAR(36),
  CUSTOMER_ID   NUMBER(10),
  PRODUCT_ID    NUMBER(6),
  ORDER_STATUS  VARCHAR(20),
  ORDER_DATE    TIMESTAMP_NTZ,
  REGION        VARCHAR(20),
  AMOUNT        DECIMAL(10,2),
  QUANTITY      NUMBER(5),
  CREATED_AT    TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);
SELECT COUNT(*) AS row_count, MAX(CREATED_AT) AS max_created_at FROM RAW_DB.PUBLIC.RAW_ORDERS;

CREATE OR REPLACE TABLE RAW_DB.PUBLIC.RAW_EVENTS (
  EVENT_ID         VARCHAR(36),
  CUSTOMER_ID      NUMBER(10),
  EVENT_TYPE       VARCHAR(30),
  SESSION_ID       VARCHAR(36),
  EVENT_TIMESTAMP  TIMESTAMP_NTZ,
  PAGE_URL         VARCHAR(500),
  DEVICE_TYPE      VARCHAR(20)
);
SELECT COUNT(*) AS row_count, MAX(EVENT_TIMESTAMP) AS max_created_at FROM RAW_DB.PUBLIC.RAW_EVENTS;

CREATE OR REPLACE TABLE RAW_DB.PUBLIC.RAW_PRODUCTS (
  PRODUCT_ID    NUMBER(6),
  PRODUCT_NAME  VARCHAR(200),
  CATEGORY      VARCHAR(50),
  SUB_CATEGORY  VARCHAR(50),
  UNIT_PRICE    DECIMAL(10,2),
  COST_PRICE    DECIMAL(10,2),
  SUPPLIER_ID   NUMBER(6),
  IS_ACTIVE     BOOLEAN,
  UPDATED_AT    TIMESTAMP_NTZ
);
SELECT COUNT(*) AS row_count, MAX(UPDATED_AT) AS max_created_at FROM RAW_DB.PUBLIC.RAW_PRODUCTS;

CREATE OR REPLACE TABLE RAW_DB.PUBLIC.RAW_CUSTOMERS (
  CUSTOMER_ID   NUMBER(10),
  FIRST_NAME    VARCHAR(50),
  LAST_NAME     VARCHAR(50),
  EMAIL         VARCHAR(100),
  CITY          VARCHAR(50),
  COUNTRY       VARCHAR(50),
  TIER          VARCHAR(20),
  CREATED_AT    TIMESTAMP_NTZ,
  UPDATED_AT    TIMESTAMP_NTZ
);
SELECT COUNT(*) AS row_count, MAX(CREATED_AT) AS max_created_at FROM RAW_DB.PUBLIC.RAW_CUSTOMERS;

