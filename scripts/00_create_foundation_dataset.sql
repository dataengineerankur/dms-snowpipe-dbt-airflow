SELECT 'RAW_ORDERS' AS table_name, SYSTEM$CLUSTERING_INFORMATION('RAW_DB.PUBLIC.RAW_ORDERS', '(ORDER_DATE::DATE, REGION)') AS clustering_info
UNION ALL
SELECT 'RAW_EVENTS', SYSTEM$CLUSTERING_INFORMATION('RAW_DB.PUBLIC.RAW_EVENTS', '(EVENT_TIMESTAMP::DATE, EVENT_TYPE)')
UNION ALL
SELECT 'RAW_PRODUCTS', SYSTEM$CLUSTERING_INFORMATION('RAW_DB.PUBLIC.RAW_PRODUCTS', '(CATEGORY, UPDATED_AT::DATE)')
UNION ALL
SELECT 'RAW_CUSTOMERS', SYSTEM$CLUSTERING_INFORMATION('RAW_DB.PUBLIC.RAW_CUSTOMERS', '(COUNTRY, TIER)');

SELECT DATEDIFF('second', $START_TS, CURRENT_TIMESTAMP()) AS elapsed_seconds;
