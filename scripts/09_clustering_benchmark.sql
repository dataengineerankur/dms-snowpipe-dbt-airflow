USE ROLE ACCOUNTADMIN;
USE WAREHOUSE COMPUTE_WH;
SET start_ts = CURRENT_TIMESTAMP();
CREATE DATABASE IF NOT EXISTS BENCHMARKS;
CREATE SCHEMA IF NOT EXISTS BENCHMARKS.PUBLIC;
USE DATABASE BENCHMARKS;

CREATE OR REPLACE TABLE BENCHMARKS.PUBLIC.RAW_ORDERS_NO_CLUSTER AS SELECT * FROM RAW_DB.PUBLIC.RAW_ORDERS;
SELECT COUNT(*), MAX(CREATED_AT) FROM BENCHMARKS.PUBLIC.RAW_ORDERS_NO_CLUSTER;

CREATE OR REPLACE TABLE BENCHMARKS.PUBLIC.RAW_ORDERS_DATE_CLUSTERED CLUSTER BY (ORDER_DATE::DATE) AS SELECT * FROM RAW_DB.PUBLIC.RAW_ORDERS;
SELECT COUNT(*), MAX(CREATED_AT) FROM BENCHMARKS.PUBLIC.RAW_ORDERS_DATE_CLUSTERED;

CREATE OR REPLACE TABLE BENCHMARKS.PUBLIC.RAW_ORDERS_REGION_CLUSTERED CLUSTER BY (REGION) AS SELECT * FROM RAW_DB.PUBLIC.RAW_ORDERS;
SELECT COUNT(*), MAX(CREATED_AT) FROM BENCHMARKS.PUBLIC.RAW_ORDERS_REGION_CLUSTERED;

CREATE OR REPLACE TABLE BENCHMARKS.PUBLIC.RAW_ORDERS_COMPOUND_CLUSTERED CLUSTER BY (REGION, ORDER_DATE::DATE) AS SELECT * FROM RAW_DB.PUBLIC.RAW_ORDERS;
SELECT COUNT(*), MAX(CREATED_AT) FROM BENCHMARKS.PUBLIC.RAW_ORDERS_COMPOUND_CLUSTERED;

CREATE OR REPLACE TABLE BENCHMARKS.PUBLIC.RAW_ORDERS_BAD_CLUSTER CLUSTER BY (CUSTOMER_ID) AS SELECT * FROM RAW_DB.PUBLIC.RAW_ORDERS;
SELECT COUNT(*), MAX(CREATED_AT) FROM BENCHMARKS.PUBLIC.RAW_ORDERS_BAD_CLUSTER;

CREATE OR REPLACE TABLE BENCHMARKS.PUBLIC.CLUSTERING_RESULTS (
  table_type STRING, query_type STRING, query_id STRING, partitions_scanned NUMBER,
  partitions_total NUMBER, pct_scanned FLOAT, bytes_scanned NUMBER, execution_time NUMBER,
  recorded_at TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

SELECT COUNT(*), SUM(AMOUNT) FROM BENCHMARKS.PUBLIC.RAW_ORDERS_DATE_CLUSTERED WHERE ORDER_DATE BETWEEN '2023-01-01' AND '2023-03-31';
INSERT INTO BENCHMARKS.PUBLIC.CLUSTERING_RESULTS
SELECT 'date_clustered', 'date_range', query_id, NULL AS partitions_scanned, NULL AS partitions_total,
       NULL AS pct_scanned, bytes_scanned, execution_time, CURRENT_TIMESTAMP()
FROM TABLE(INFORMATION_SCHEMA.QUERY_HISTORY_BY_SESSION()) ORDER BY start_time DESC LIMIT 1;

SELECT SYSTEM$CLUSTERING_DEPTH('BENCHMARKS.PUBLIC.RAW_ORDERS_DATE_CLUSTERED');
SELECT SYSTEM$CLUSTERING_INFORMATION('BENCHMARKS.PUBLIC.RAW_ORDERS_COMPOUND_CLUSTERED', '(REGION, ORDER_DATE::DATE)');
SELECT * FROM BENCHMARKS.PUBLIC.CLUSTERING_RESULTS ORDER BY query_type, partitions_scanned;
SELECT DATEDIFF('second', $start_ts, CURRENT_TIMESTAMP()) AS elapsed_seconds;
