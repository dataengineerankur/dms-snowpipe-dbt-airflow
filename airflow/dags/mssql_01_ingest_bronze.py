"""
DAG 1 of 4 — MSSQL Migration: Ingest to Bronze
================================================
SOURCE  : S3 bucket (DMS full-load Parquet files)
          RAW_MSSQL.RAW_DMS_VARIANT (Snowflake VARIANT table)
TARGET  : MSSQL_MIGRATION_LAB.BRONZE.*  (15 typed tables)

What it does
------------
Step 1  copy_raw_from_s3     COPY INTO RAW_MSSQL.RAW_DMS_VARIANT from S3 stage
Step 2  check_raw_count      Assert > 0 rows landed in RAW (fail-fast gate)
Step 3  merge_bronze_stress  MERGE → BRONZE.CUSTOMERS / PRODUCTS / CATEGORIES /
                                     ORDERS / ORDER_ITEMS
Step 4  merge_bronze_erp     MERGE → BRONZE.ERP_DEPARTMENTS / ERP_EMPLOYEES /
                                     ERP_PAYROLL_RUNS / ERP_PAYROLL_LINES
Step 5  merge_bronze_crm     MERGE → BRONZE.CRM_ACCOUNTS / CRM_CONTACTS /
                                     CRM_OPPORTUNITIES
Step 6  merge_bronze_inv     MERGE → BRONZE.INV_WAREHOUSES / INV_SKU /
                                     INV_STOCK_MOVEMENTS
Step 7  report_bronze_counts SELECT COUNT(*) for each Bronze table

Schedule: @daily (runs once per day after DMS dump lands in S3)
Trigger:  Can also be triggered manually or via S3 event sensor
"""
from __future__ import annotations
import os
from datetime import datetime, timedelta
from airflow import DAG
from airflow.providers.snowflake.operators.snowflake import SnowflakeOperator

CONN   = "snowflake_default"
DB     = "MSSQL_MIGRATION_LAB"
RAW    = "RAW_MSSQL"
BRZ    = "BRONZE"
WH     = "WH_MSSQL_MIGRATION"
ROLE   = "ACCOUNTADMIN"

ARGS = {"owner": "data-platform", "retries": 1, "retry_delay": timedelta(minutes=5)}

# ── SQL blocks ────────────────────────────────────────────────────────────────

COPY_RAW = f"""
COPY INTO {DB}.{RAW}.RAW_DMS_VARIANT(V)
FROM @{DB}.{RAW}.STG_DMS_MSSQL
FILE_FORMAT = (TYPE = PARQUET)
FORCE = FALSE;
"""

CHECK_RAW = f"SELECT COUNT(*) AS raw_rows FROM {DB}.{RAW}.RAW_DMS_VARIANT;"

MERGE_STRESS = f"""
USE DATABASE {DB}; USE WAREHOUSE {WH};

MERGE INTO {BRZ}.CUSTOMERS tgt
USING (SELECT DISTINCT
    v:CustomerId::NUMBER AS CUSTOMER_ID, v:CustomerCode::VARCHAR(20) AS CUSTOMER_CODE,
    v:FullName::VARCHAR(200) AS FULL_NAME, v:Email::VARCHAR(320) AS EMAIL,
    v:Country::VARCHAR(100) AS COUNTRY, v:CreatedAt::TIMESTAMP_NTZ AS CREATED_AT,
    'I' AS _DMS_OPERATION, CURRENT_TIMESTAMP() AS _LOADED_AT,
    'SnowConvertStressDB' AS _SOURCE_DB
    FROM {RAW}.RAW_DMS_VARIANT WHERE v:CustomerId IS NOT NULL) src
ON tgt.CUSTOMER_ID=src.CUSTOMER_ID AND tgt._SOURCE_DB=src._SOURCE_DB
WHEN MATCHED AND src._LOADED_AT>tgt._LOADED_AT THEN UPDATE SET
    FULL_NAME=src.FULL_NAME, EMAIL=src.EMAIL, COUNTRY=src.COUNTRY, _LOADED_AT=src._LOADED_AT
WHEN NOT MATCHED THEN INSERT
    (CUSTOMER_ID,CUSTOMER_CODE,FULL_NAME,EMAIL,COUNTRY,CREATED_AT,_DMS_OPERATION,_LOADED_AT,_SOURCE_DB)
    VALUES(src.CUSTOMER_ID,src.CUSTOMER_CODE,src.FULL_NAME,src.EMAIL,src.COUNTRY,
           src.CREATED_AT,src._DMS_OPERATION,src._LOADED_AT,src._SOURCE_DB);

MERGE INTO {BRZ}.CATEGORIES tgt
USING (SELECT DISTINCT v:CategoryId::NUMBER AS CATEGORY_ID,
    v:CategoryName::VARCHAR(100) AS CATEGORY_NAME, v:Description::VARCHAR(500) AS DESCRIPTION,
    'I' AS _DMS_OPERATION, CURRENT_TIMESTAMP() AS _LOADED_AT, 'SnowConvertStressDB' AS _SOURCE_DB
    FROM {RAW}.RAW_DMS_VARIANT WHERE v:CategoryId IS NOT NULL AND v:CategoryName IS NOT NULL) src
ON tgt.CATEGORY_ID=src.CATEGORY_ID AND tgt._SOURCE_DB=src._SOURCE_DB
WHEN NOT MATCHED THEN INSERT (CATEGORY_ID,CATEGORY_NAME,DESCRIPTION,_DMS_OPERATION,_LOADED_AT,_SOURCE_DB)
    VALUES(src.CATEGORY_ID,src.CATEGORY_NAME,src.DESCRIPTION,src._DMS_OPERATION,src._LOADED_AT,src._SOURCE_DB);

MERGE INTO {BRZ}.PRODUCTS tgt
USING (SELECT DISTINCT v:ProductId::NUMBER AS PRODUCT_ID, v:CategoryId::NUMBER AS CATEGORY_ID,
    v:SKU::VARCHAR(50) AS SKU, v:ProductName::VARCHAR(200) AS PRODUCT_NAME,
    v:ListPrice::NUMBER(18,4) AS LIST_PRICE, v:IsActive::BOOLEAN AS IS_ACTIVE,
    'I' AS _DMS_OPERATION, CURRENT_TIMESTAMP() AS _LOADED_AT, 'SnowConvertStressDB' AS _SOURCE_DB
    FROM {RAW}.RAW_DMS_VARIANT WHERE v:ProductId IS NOT NULL) src
ON tgt.PRODUCT_ID=src.PRODUCT_ID AND tgt._SOURCE_DB=src._SOURCE_DB
WHEN MATCHED AND src._LOADED_AT>tgt._LOADED_AT THEN UPDATE SET
    LIST_PRICE=src.LIST_PRICE,IS_ACTIVE=src.IS_ACTIVE,_LOADED_AT=src._LOADED_AT
WHEN NOT MATCHED THEN INSERT
    (PRODUCT_ID,CATEGORY_ID,SKU,PRODUCT_NAME,LIST_PRICE,IS_ACTIVE,_DMS_OPERATION,_LOADED_AT,_SOURCE_DB)
    VALUES(src.PRODUCT_ID,src.CATEGORY_ID,src.SKU,src.PRODUCT_NAME,src.LIST_PRICE,
           src.IS_ACTIVE,src._DMS_OPERATION,src._LOADED_AT,src._SOURCE_DB);

MERGE INTO {BRZ}.ORDERS tgt
USING (SELECT DISTINCT v:OrderId::NUMBER AS ORDER_ID, v:CustomerId::NUMBER AS CUSTOMER_ID,
    v:OrderDate::DATE AS ORDER_DATE, v:Status::VARCHAR(30) AS STATUS,
    v:TotalAmount::NUMBER(18,4) AS TOTAL_AMOUNT, v:Notes::VARCHAR(500) AS NOTES,
    'I' AS _DMS_OPERATION, CURRENT_TIMESTAMP() AS _LOADED_AT, 'SnowConvertStressDB' AS _SOURCE_DB
    FROM {RAW}.RAW_DMS_VARIANT WHERE v:OrderId IS NOT NULL) src
ON tgt.ORDER_ID=src.ORDER_ID AND tgt._SOURCE_DB=src._SOURCE_DB
WHEN MATCHED AND src._LOADED_AT>tgt._LOADED_AT THEN UPDATE SET
    STATUS=src.STATUS,TOTAL_AMOUNT=src.TOTAL_AMOUNT,NOTES=src.NOTES,_LOADED_AT=src._LOADED_AT
WHEN NOT MATCHED THEN INSERT
    (ORDER_ID,CUSTOMER_ID,ORDER_DATE,STATUS,TOTAL_AMOUNT,NOTES,_DMS_OPERATION,_LOADED_AT,_SOURCE_DB)
    VALUES(src.ORDER_ID,src.CUSTOMER_ID,src.ORDER_DATE,src.STATUS,src.TOTAL_AMOUNT,
           src.NOTES,src._DMS_OPERATION,src._LOADED_AT,src._SOURCE_DB);

MERGE INTO {BRZ}.ORDER_ITEMS tgt
USING (SELECT DISTINCT v:OrderItemId::NUMBER AS ORDER_ITEM_ID, v:OrderId::NUMBER AS ORDER_ID,
    v:ProductId::NUMBER AS PRODUCT_ID, v:Quantity::NUMBER AS QUANTITY,
    v:UnitPrice::NUMBER(18,4) AS UNIT_PRICE,
    COALESCE(v:LineTotal::NUMBER(18,4), v:Quantity::NUMBER*v:UnitPrice::NUMBER(18,4)) AS LINE_TOTAL,
    'I' AS _DMS_OPERATION, CURRENT_TIMESTAMP() AS _LOADED_AT, 'SnowConvertStressDB' AS _SOURCE_DB
    FROM {RAW}.RAW_DMS_VARIANT WHERE v:OrderItemId IS NOT NULL) src
ON tgt.ORDER_ITEM_ID=src.ORDER_ITEM_ID AND tgt._SOURCE_DB=src._SOURCE_DB
WHEN NOT MATCHED THEN INSERT
    (ORDER_ITEM_ID,ORDER_ID,PRODUCT_ID,QUANTITY,UNIT_PRICE,LINE_TOTAL,_DMS_OPERATION,_LOADED_AT,_SOURCE_DB)
    VALUES(src.ORDER_ITEM_ID,src.ORDER_ID,src.PRODUCT_ID,src.QUANTITY,src.UNIT_PRICE,
           src.LINE_TOTAL,src._DMS_OPERATION,src._LOADED_AT,src._SOURCE_DB);
"""

