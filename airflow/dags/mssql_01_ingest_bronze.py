REPORT = f"""
SELECT table_name, row_count FROM (
    SELECT 'CUSTOMERS' AS table_name, COUNT(*) AS row_count FROM {DB}.{BRZ}.CUSTOMERS UNION ALL
    SELECT 'PRODUCTS',  COUNT(*) FROM {DB}.{BRZ}.PRODUCTS  UNION ALL
    SELECT 'CATEGORIES',COUNT(*) FROM {DB}.{BRZ}.CATEGORIES UNION ALL
    SELECT 'ORDERS',    COUNT(*) FROM {DB}.{BRZ}.ORDERS     UNION ALL
    SELECT 'ORDER_ITEMS',COUNT(*) FROM {DB}.{BRZ}.ORDER_ITEMS UNION ALL
    SELECT 'ERP_EMPLOYEES',COUNT(*) FROM {DB}.{BRZ}.ERP_EMPLOYEES UNION ALL
    SELECT 'ERP_PAYROLL_LINES',COUNT(*) FROM {DB}.{BRZ}.ERP_PAYROLL_LINES UNION ALL
    SELECT 'CRM_ACCOUNTS',COUNT(*) FROM {DB}.{BRZ}.CRM_ACCOUNTS UNION ALL
    SELECT 'CRM_OPPORTUNITIES',COUNT(*) FROM {DB}.{BRZ}.CRM_OPPORTUNITIES UNION ALL
    SELECT 'INV_SKU',   COUNT(*) FROM {DB}.{BRZ}.INV_SKU UNION ALL
    SELECT 'INV_STOCK_MOVEMENTS',COUNT(*) FROM {DB}.{BRZ}.INV_STOCK_MOVEMENTS
) ORDER BY table_name;
"""

with DAG(
    dag_id="mssql_01_ingest_bronze",
    default_args=ARGS,
    start_date=datetime(2024, 1, 1),
    schedule_interval="@daily",
    catchup=False,
    tags=["mssql-migration", "bronze", "ingest"],
    doc_md=__doc__,
) as dag:

    copy_raw   = SnowflakeOperator(task_id="copy_raw_from_s3",    snowflake_conn_id=CONN, sql=COPY_RAW,      warehouse=WH, database=DB, role=ROLE)
    check_raw  = SnowflakeOperator(task_id="check_raw_count",     snowflake_conn_id=CONN, sql=CHECK_RAW,     warehouse=WH, database=DB)
    mrg_stress = SnowflakeOperator(task_id="merge_bronze_stress", snowflake_conn_id=CONN, sql=MERGE_STRESS,  warehouse=WH, database=DB, role=ROLE)
    mrg_erp    = SnowflakeOperator(task_id="merge_bronze_erp",    snowflake_conn_id=CONN, sql=MERGE_ERP,     warehouse=WH, database=DB, role=ROLE)
    mrg_crm    = SnowflakeOperator(task_id="merge_bronze_crm",    snowflake_conn_id=CONN, sql=MERGE_CRM,     warehouse=WH, database=DB, role=ROLE)
    mrg_inv    = SnowflakeOperator(task_id="merge_bronze_inv",    snowflake_conn_id=CONN, sql=MERGE_INV,     warehouse=WH, database=DB, role=ROLE)
    report     = SnowflakeOperator(task_id="report_bronze_counts",snowflake_conn_id=CONN, sql=REPORT,        warehouse=WH, database=DB)

    copy_raw >> check_raw >> [mrg_stress, mrg_erp, mrg_crm, mrg_inv] >> report
